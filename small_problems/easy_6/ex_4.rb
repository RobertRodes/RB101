def reverse!(arr)
  arr_temp = arr.clone
  arr.clear
  
  arr.push arr_temp.pop until arr_temp.size.zero?
end
  
  
p reverse!([1,2,3,4,5])

list = [1,2,3,4]
result = reverse!(list)
p result == [4, 3, 2, 1]
p list == [4, 3, 2, 1]
p list.object_id == result.object_id

list = %w(a b e d c)
p reverse!(list) == ["c", "d", "e", "b", "a"]
p list == ["c", "d", "e", "b", "a"]

list = ['abc']
p reverse!(list) == ["abc"]
p list == ["abc"]

list = []
p reverse!(list) == []
p list == []